#include<iostream>

int main(){

    int x;//declaration
    x=5;//assigning value

    int y=6;

    std::cout<<y<<'\n';
    std::cout<<x<<'\n';

    int sum=x+y;
    std::cout<<sum<<'\n';

    int decimal=7.55;
    std::cout<<decimal<<'\n';

    //double is for printing decimals
    double GPA=3.12;
    std::cout<<GPA<<'\n';


    //char is for single character

    char grade='A';
    char initial='$';

    std::cout<<grade<<'\n';
    std::cout<<initial<<'\n';

    //bool is for true and false

    bool student=true;
    bool power=false;

    std::cout<<student<<'\n';
    std::cout<<power<<'\n';

    //string is for a word or sentence

    std::string name="Ansh";
    
    std::string day="Friday";

    std::cout<<name<<'\n';
    std::cout<<day<<'\n';

    return 0;


}
